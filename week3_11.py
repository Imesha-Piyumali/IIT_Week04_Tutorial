# Example of basic Python data types

# String: text like "hello"
greeting = "hello"
print("String example:", greeting)

# Integer: a whole number like 3
age = 3
print("Integer example:", age)

# Float: a decimal number like 3.5
pi_value = 3.5
print("Float example:", pi_value)

# Variable: a named box that stores a value
# (all the above are stored in variables)
name = "Imesha"
print("Variable example (name):", name)

# List: an ordered collection like ["a", "b", "c"]
letters = ["a", "b", "c"]
print("List example:", letters)

# Bonus: Accessing list elements
print("First item in the list:", letters[0])
